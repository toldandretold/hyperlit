// Ambient data-flow visualiser (canvas). Uses the EXACT max-mode layout of the real map (positions
// extracted headless) and the same edge colours. It SLOWLY crossfades between the two pipelines —
// "to DOM" (load: pull/read/domwrite, pulses sweeping DOWN) and "to PostgreSQL" (save: push/domread/
// write, pulses sweeping UP) — fading one in as the other fades out, forever. Both sides PULSE the
// whole time (traveling waves), over a faint always-on substrate of the full graph. Edges only —
// nodes are just faint dots.
//
//   node visualisation/experiments/build-flow-lines.mjs   →   visualisation/experiments/flow-lines.html
//
// Standalone: not wired into `npm run viz:idb`, not byte-checked — can't affect the real map.

import fs from 'node:fs';
import path from 'node:path';
import url from 'node:url';
import { chromium } from 'playwright';

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '../..');

const REL_COLOR = { read: '#5eb0ef', write: '#54c98a', push: '#e0a44b', pull: '#b07ad6', domread: '#3fb6b6', domwrite: '#e06a9a', handoff: '#5fb3a3' };
const LOAD = new Set(['pull', 'read', 'domwrite']);   // to DOM (downward)
const SAVE = new Set(['push', 'domread', 'write']);   // to Postgres (upward)
const RENDER_KINDS = new Set(['fn', 'store', 'table', 'route', 'controller', 'dom']);
const KIND_COLOR = { table: '#b07ad6', controller: '#c45d6d', route: '#8aa0c0', store: '#54c98a', dom: '#6f8bd6', fn: '#586074' };

// ── 1. exact max-mode node positions, straight out of the real map ──
const b = await chromium.launch();
const pg = await b.newPage({ viewport: { width: 1800, height: 1400 } });
await pg.goto('file://' + path.join(ROOT, 'visualisation/generated/full-stack-data-map.html'));
await pg.waitForTimeout(900);
await pg.evaluate(() => { const z = document.getElementById('zoomLevel'); z.value = 'max'; z.onchange(); });
await pg.waitForTimeout(1300);
const positions = await pg.evaluate(() => cy.nodes().map(n => ({ id: n.id(), x: n.position('x'), y: n.position('y'), kind: n.data('kind') })));
await b.close();

const posOf = new Map();
for (const n of positions) if (RENDER_KINDS.has(n.kind)) posOf.set(n.id, n);

// ── 2. read the graph from the map's generated JSON (edges) ──
const viz = JSON.parse(fs.readFileSync(path.join(ROOT, 'visualisation/generated/flowViz.generated.json'), 'utf8'));
const nodes = [];
const nodeIndex = new Map();
for (const [id, n] of posOf) { nodeIndex.set(id, nodes.length); nodes.push({ id, x: n.x, y: n.y, kind: n.kind }); }

let minX = Infinity, maxX = -Infinity, minY = Infinity, maxY = -Infinity;
for (const n of nodes) { minX = Math.min(minX, n.x); maxX = Math.max(maxX, n.x); minY = Math.min(minY, n.y); maxY = Math.max(maxY, n.y); }
const nx = x => (x - minX) / (maxX - minX), ny = y => (y - minY) / (maxY - minY);

const edges = [];
const seen = new Set();
for (const e of viz.edges) {
  let rel = e.rel, klass;
  if (rel === 'call') { if (!e.dataPath) continue; rel = 'handoff'; klass = 2; }
  else if (LOAD.has(rel)) klass = 0;
  else if (SAVE.has(rel)) klass = 1;
  else continue;
  if (!nodeIndex.has(e.source) || !nodeIndex.has(e.target)) continue;
  const key = e.source + '|' + e.target + '|' + rel;
  if (seen.has(key)) continue; seen.add(key);
  edges.push({ s: nodeIndex.get(e.source), t: nodeIndex.get(e.target), rel, klass });
}

const payload = {
  aspect: (maxX - minX) / (maxY - minY),
  nodes: nodes.map(n => ({ x: +nx(n.x).toFixed(4), y: +ny(n.y).toFixed(4), c: KIND_COLOR[n.kind] || '#586074' })),
  edges: edges.map((e, i) => {
    const yf = (ny(nodes[e.s].y) + ny(nodes[e.t].y)) / 2;       // vertical midpoint (0 = PG top, 1 = DOM bottom)
    return { s: e.s, t: e.t, c: REL_COLOR[e.rel], k: e.klass, yf: +yf.toFixed(4), ph: +(((i * 73) % 100) / 100 * 6.283).toFixed(3) };
  }),
};

// the HyperLIT wordmark, centred — letters recoloured to the bg (#070a10) so the word reads as a
// shadow silhouette punched through the flowing lines; the two gradient squares stay as they are.
let LOGO = '';
try {
  LOGO = fs.readFileSync('/Users/samuelnicholls/Library/Mobile Documents/com~apple~CloudDocs/HyperLIT/IO design comapny correspondence/Finished Art/Logo/svg/1407_LIB Branding_Logo_01_Logo Grey Grad 3.svg', 'utf8')
    .replace(/<\?xml[^>]*\?>/, '').replaceAll('#f1f2f2', '#070a10').trim();
} catch (e) { console.warn('logo SVG not found — skipping centre logo'); }

const html = `<!doctype html>
<!-- GENERATED by visualisation/experiments/build-flow-lines.mjs. Ambient toy; regenerate: node visualisation/experiments/build-flow-lines.mjs -->
<html lang="en"><head><meta charset="utf-8"><meta name="viewport" content="width=device-width, initial-scale=1">
<title>Hyperlit — data flow</title>
<style>
  html,body{margin:0;height:100%;background:#070a10;overflow:hidden;font:12px ui-sans-serif,system-ui,sans-serif;color:#8b93a7;}
  #c{display:block;width:100vw;height:100vh;}
  #logo{position:fixed;left:50%;top:40%;transform:translate(-50%,-50%);width:min(476px,34vw);pointer-events:none;z-index:3;}
  #logo svg{width:100%;height:auto;display:block;}
</style></head><body>
<canvas id="c"></canvas>
<div id="logo">${LOGO}</div>
<script>
var DATA = ${JSON.stringify(payload)};
var NODES = DATA.nodes, EDGES = DATA.edges;
var PERIOD = 19;     // seconds for a full load → save → load crossfade (long & slow)
var WAVE_V = 0.12;   // pulse travel speed (vertical fraction / sec)
var WAVE_N = 2.3;    // number of pulse bands across the height
var cv = document.getElementById("c"), ctx = cv.getContext("2d");
var W = 0, H = 0, DPR = Math.min(2, window.devicePixelRatio || 1), ox = 0, oy = 0, sc = 1;

function layout(){
  W = cv.clientWidth; H = cv.clientHeight;
  cv.width = W*DPR; cv.height = H*DPR; ctx.setTransform(DPR,0,0,DPR,0,0);
  var pad = 28;
  sc = Math.min((W - 2*pad)/DATA.aspect, (H - 2*pad));   // fit the map's proportions
  ox = (W - sc*DATA.aspect)/2; oy = (H - sc)/2;
}
function PX(n){ return n.x*sc*DATA.aspect + ox; }
function PY(n){ return n.y*sc + oy; }
window.addEventListener("resize", layout);

var TAU = 6.28318;
var paused = false, t0 = null, tHold = 0;
function frame(now){
  if (t0 === null) t0 = now;
  var t = (paused ? tHold : (now - t0)) / 1000;
  // slow continuous crossfade: s = save weight (0 → 1 → 0), eased cosine so it lingers at the turns.
  var s = 0.5 - 0.5*Math.cos(TAU * (t/PERIOD));
  var l = 1 - s;

  ctx.setTransform(DPR,0,0,DPR,0,0);
  ctx.globalCompositeOperation = "source-over";
  ctx.fillStyle = "#070a10"; ctx.fillRect(0,0,W,H);

  // faint always-on substrate — the whole graph structure
  ctx.lineWidth = 0.5; ctx.globalAlpha = 0.06;
  for (var i=0;i<EDGES.length;i++){ var e=EDGES[i];
    ctx.strokeStyle = e.c; ctx.beginPath();
    ctx.moveTo(PX(NODES[e.s]),PY(NODES[e.s])); ctx.lineTo(PX(NODES[e.t]),PY(NODES[e.t])); ctx.stroke();
  }
  // faint node dots (structure cue; not animated)
  ctx.globalAlpha = 0.18;
  for (var n2=0;n2<NODES.length;n2++){ ctx.fillStyle = NODES[n2].c;
    ctx.beginPath(); ctx.arc(PX(NODES[n2]),PY(NODES[n2]),1.3,0,TAU); ctx.fill(); }

  // brightness per edge = its set's crossfade weight × a traveling-wave pulse (load sweeps down, save up)
  var bArr = frame._b || (frame._b = new Float32Array(EDGES.length));
  for (var j=0;j<EDGES.length;j++){ var ed=EDGES[j];
    var w = ed.k===0 ? l : ed.k===1 ? s : Math.min(l,s)*1.1;   // hand-offs peak mid-blend
    if (w < 0.015){ bArr[j]=0; continue; }
    var sweep = ed.k===1 ? (ed.yf + t*WAVE_V) : (ed.yf - t*WAVE_V);   // save up · load (+handoff) down
    var pulse = 0.30 + 0.70*Math.max(0, Math.sin(sweep*TAU*WAVE_N + ed.ph));
    bArr[j] = w * pulse;
  }
  // soft additive halo for the bright ones (only additive pass)
  ctx.globalCompositeOperation = "lighter"; ctx.lineWidth = 3.0;
  for (var h=0;h<EDGES.length;h++){ var b1=bArr[h]; if (b1<0.18) continue; var e1=EDGES[h];
    ctx.globalAlpha = b1*0.13; ctx.strokeStyle = e1.c; ctx.beginPath();
    ctx.moveTo(PX(NODES[e1.s]),PY(NODES[e1.s])); ctx.lineTo(PX(NODES[e1.t]),PY(NODES[e1.t])); ctx.stroke();
  }
  // true-colour core (source-over → no white-out where many overlap)
  ctx.globalCompositeOperation = "source-over"; ctx.lineWidth = 1.2;
  for (var c2=0;c2<EDGES.length;c2++){ var b2=bArr[c2]; if (b2<0.05) continue; var e2=EDGES[c2];
    ctx.globalAlpha = Math.min(0.88, 0.15 + b2*0.7); ctx.strokeStyle = e2.c; ctx.beginPath();
    ctx.moveTo(PX(NODES[e2.s]),PY(NODES[e2.s])); ctx.lineTo(PX(NODES[e2.t]),PY(NODES[e2.t])); ctx.stroke();
  }
  ctx.globalAlpha = 1;
  if (!paused) tHold = now - t0;
  requestAnimationFrame(frame);
}

function toggle(){ paused=!paused; }
cv.addEventListener("click", toggle);
window.addEventListener("keydown", function(ev){ if(ev.code==="Space"){ ev.preventDefault(); toggle(); } });

layout();
requestAnimationFrame(frame);
</script></body></html>`;

const outPath = path.join(__dirname, 'flow-lines.html');
fs.writeFileSync(outPath, html);
console.log('wrote ' + outPath);
console.log('  nodes ' + nodes.length + ' · edges ' + edges.length +
  ' (load ' + edges.filter(e => e.klass === 0).length + ' · save ' + edges.filter(e => e.klass === 1).length + ' · handoff ' + edges.filter(e => e.klass === 2).length + ')');
